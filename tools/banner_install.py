def _detect_claude_cli_version(claude_path):
    """F9: best-effort INFORMATIONAL version tag carried in the stamp. The
    stamp's authoritative key is the whole-file hash (banner.py's
    _classify_patch_plan compares patched_sha256, never version) -- this
    is metadata only, never load-bearing for correctness, and swallows
    every failure rather than blocking the install over a `--version`
    hiccup."""
    try:
        result = subprocess.run([claude_path, "--version"], capture_output=True,
                                 text=True, timeout=10)
        if result.returncode == 0:
            value = result.stdout.strip()
            return value or None
    except Exception:
        pass
    return None


def report_patch_plan(home=None, target_path=None):
    """Human-readable preview lines for the confirm step, built from
    banner.py's side-effect-free patch_report(). Never writes anything.

    F3: includes the exact target path AND its whole-file sha256. This is
    the confirm-to-write binding surface -- commands/banner.md captures
    both from this output and passes them back as --target/--expect-hash
    on the subsequent confirmed `install --yes` call, so apply_patch can
    refuse if either changed between preview and confirmation."""
    plan = banner_patch.patch_report(target_path=target_path, home=home)
    lines = [f"binary patch plan: {plan['status']}"]
    if plan.get("target"):
        lines.append(f"  target binary: {plan['target']}")
    if plan.get("target_sha256"):
        lines.append(f"  target sha256: {plan['target_sha256']}")
    if plan.get("count") is not None:
        lines.append(f"  escapes found: {plan['count']}"
                      + (f" (form: {plan['form']})" if plan.get("form") else ""))
    if plan.get("journal_path"):
        lines.append(f"  offsets journal would be written to: {plan['journal_path']}")
    if plan.get("stamp_path"):
        lines.append(f"  stamp would be written to: {plan['stamp_path']}")
    lines.append(f"  {plan['message']}")
    lines.append(f"  {banner_patch.DISCLOSURE}")
    return lines, plan


# ---------------------------------------------------------------------------
# Legacy-artifact detection (3rd real-machine-incident hardening).
#
# GROUND TRUTH: a real machine was found carrying a broken claude.bat (the
# literal TEST-FIXTURE placeholder path 'C:\real\claude.exe' baked into its
# target line) plus a forge-autorun.cmd wiring it into the HKCU AutoRun
# doskey macro -- doskey beats PATH for an interactive cmd.exe session, so
# this silently hijacked and broke the `claude` command in every CMD
# window, while `where claude` kept reporting the correct, working install
# because PATH resolution itself was never touched. These pure extraction
# helpers + scan_legacy_artifacts() detect exactly that shape so
# `/forge:banner status` can report it and `/forge:banner install` can
# refuse to layer a fresh install on top of it (see the preflight check in
# install_all below).
# ---------------------------------------------------------------------------

_BAT_TARGET_RE = re.compile(r'^"([^"]+)" %\*\s*$', re.MULTILINE)
_AUTORUN_TARGET_RE = re.compile(r'doskey claude=call "([^"]+)" \$\*')


def _extract_bat_target(text):
    """Pure: pull the claude CLI path a generated claude.bat launches, out
    of its own text -- the line build_claude_bat() emits as `"<path>" %*`.
    Returns None if the shape isn't found (not one of ours, or corrupted)."""
    match = _BAT_TARGET_RE.search(text or "")
    return match.group(1) if match else None


def _extract_autorun_target(text):
    """Pure: pull the claude.bat path a generated forge-autorun.cmd's
    doskey macro points at, out of its own text. Returns None if the shape
    isn't found."""
    match = _AUTORUN_TARGET_RE.search(text or "")
    return match.group(1) if match else None


def _is_ours_by_location(referenced_path, shim_dir):
    """Verify-bounce P1 fix support: when an AutoRun-referenced path
    doesn't exist on disk any more, we can't read a tag out of it to
    confirm it's forge-authored -- so a finding about it is only GATING
    (refuses install) when it's unambiguously ours BY LOCATION: its
    basename is our AutoRun-cmd filename, or it lives directly inside our
    own shim directory. Anything else (some unrelated program's AutoRun
    entry referencing some other missing file) is real, but not ours to
    gate an install on or promise restore will clear."""
    try:
        candidate = Path(referenced_path)
    except (TypeError, ValueError):
        return False
    if candidate.name.lower() == AUTORUN_CMD_NAME.lower():
        return True
    try:
        return candidate.parent.resolve() == shim_dir.resolve()
    except OSError:
        return candidate.parent == shim_dir


def scan_legacy_artifacts(home=None, get_autorun_fn=None):
    """Report-only scan for BROKEN legacy banner-shim artifacts left behind
    by an unclean prior install/uninstall -- never writes or deletes
    anything itself (see install_all's preflight refusal and restore_all's
    existing unconditional tagged-file removal for the actions taken on
    what this finds). Returns a list of finding dicts:
    {"message": str, "gating": bool}, empty when nothing was found.

    "gating" is True only for a finding this plugin can unambiguously
    attribute to itself (a shim/AutoRun-cmd file carrying our own tag, or
    an AutoRun-referenced path that is ours by location -- see
    _is_ours_by_location) -- install_all's preflight refuses ONLY on
    gating findings. A non-gating ("informational") finding -- e.g. some
    OTHER program's AutoRun entry referencing an unrelated missing file --
    is still reported (status_all surfaces every finding) but never blocks
    an install, and restore_all cannot and does not promise to clear it
    (verify-bounce P1 fix: the scan previously flagged, and install
    refused on, ANY missing quoted AutoRun path regardless of whether it
    had anything to do with forge at all).

    Every filesystem/registry read here is wrapped defensively: a shim
    that isn't ours (no FORGE_BAT_TAG/FORGE_AUTORUN_TAG), an unreadable
    file, or a blocked/unavailable registry read is treated as "nothing to
    report" rather than an error -- this is a best-effort report-only scan,
    never a hard failure path."""
    findings = []

    shim_dir = default_shim_dir(home=home)
    claude_bat = shim_dir / CLAUDE_BAT_NAME
    autorun_cmd = shim_dir / AUTORUN_CMD_NAME

    try:
        if claude_bat.is_file():
            text = claude_bat.read_text(encoding="utf-8", errors="ignore")
            if FORGE_BAT_TAG in text:
                target = _extract_bat_target(text)
                if target and not Path(target).is_file():
                    findings.append({
                        "gating": True,
                        "message": (
                            f"broken legacy shim: {claude_bat} targets a "
                            f"claude CLI path that no longer exists "
                            f"({target}) -- `claude` in cmd.exe will fail "
                            "while `where claude` may still look correct, "
                            "because doskey beats PATH."
                        ),
                    })
    except OSError:
        pass

    try:
        if autorun_cmd.is_file():
            text = autorun_cmd.read_text(encoding="utf-8", errors="ignore")
            if FORGE_AUTORUN_TAG in text:
                bat_target = _extract_autorun_target(text)
                if bat_target and not Path(bat_target).is_file():
                    findings.append({
                        "gating": True,
                        "message": (
                            f"broken legacy artifact: {autorun_cmd} "
                            f"references a claude.bat that no longer "
                            f"exists ({bat_target})."
                        ),
                    })
    except OSError:
        pass

    # READ-ONLY registry query, guarded: never writes, and any failure
    # (winreg unavailable, key missing, blocked by the test hermeticity
    # guard) is swallowed -- this scan degrades to "nothing to report"
    # rather than raising, same as every other guarded surface above.
    if os.name == "nt" and winreg is not None:
        get_fn = get_autorun_fn or get_autorun_value
        try:
            existing = get_fn()
        except Exception:
            existing = None
        if existing:
            for referenced_path in re.findall(r'"([^"]+)"', existing):
                if referenced_path and not Path(referenced_path).is_file():
                    gating = _is_ours_by_location(referenced_path, shim_dir)
                    findings.append({
                        "gating": gating,
                        "message": (
                            f"{'broken AutoRun entry' if gating else 'AutoRun entry (not ours)'}: "
                            f"HKCU\\{REGISTRY_KEY_PATH} AutoRun references "
                            f"a file that no longer exists "
                            f"({referenced_path})."
                        ),
                    })

    return findings


