#!/usr/bin/env python3
"""Forge banner launcher installer/uninstaller (fg-a10904).

A Claude Code SessionStart hook cannot paint the user's terminal: its
stdout becomes model context, not terminal output. That is why
hooks/hooks.json no longer wires hooks/scripts/banner.sh into SessionStart
(root cause discovered 2026-07-18: the örn banner rendered into the model's
context every session and the user never saw a single pixel of it). The only
way to actually show the banner in a terminal is an OPT-IN shell launcher
shim that intercepts the `claude` command, prints the banner to the real
terminal, and then hands off to the real CLI. This module installs,
upgrades, uninstalls, and reports the status of that shim across every shell
surface Windows exposes. Invoked by commands/banner.md (`/forge:banner`).

Shell surfaces covered, per the live-debug ground truth captured in
.forge/queue/tasks/fg-a10904-banner-launcher.md (2026-07-18, user's machine):

  - PowerShell ($PROFILE, both `powershell` and `pwsh` if installed): a
    marker-guarded `claude` function that invokes `banner.py --anim`
    (fg-a10905) DIRECTLY -- never piped through `| Out-Host`, which would
    buffer output and defeat the animation's cursor-up redraws -- skips
    -p/--print, try/catch fail-open to a plain launch. Detects and upgrades
    a hand-rolled block using the same markers (the 2026-07-18 hand fix
    used this exact marker text).
  - cmd.exe: a shim dir (claude.bat) is NOT sufficient on its own -- if the
    real claude.exe's directory sits on the SYSTEM PATH it shadows a
    user-PATH shim, because cmd searches the system PATH first. The working
    mechanism is a doskey macro loaded via the HKCU AutoRun registry value
    (doskey only expands interactively, so `cmd /c` and scripts are
    unaffected "for free"). AutoRun is created if absent and chained
    ("existing & ours") if something else already occupies it.
  - bash/zsh (~/.bashrc, ~/.zshrc), when present: an equivalent function.

Out of scope, documented rather than handled: Win+R "claude" (the Run
dialog) launches claude.exe directly and bypasses every shim below -- there
is no interception point for that path.

Stdlib only, per the project constitution. Every shell-surface transform is
a pure, independently testable unit (marker-block upsert/remove, AutoRun
chaining) that takes plain strings and does no I/O; the thin wrappers around
them are the only things that touch the real filesystem or registry, and
registry access is behind get_autorun_value/set_autorun_value so tests can
stub it out without ever touching HKCU.
"""

