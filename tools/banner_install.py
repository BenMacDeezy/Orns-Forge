def write_shim_file(path, content, newline=""):
    """Write a generated shim/AutoRun file (claude.bat, forge-autorun.cmd)
    to disk. The cmd.exe surface's equivalent of install_into_file below --
    kept as its own named function (rather than an inline path.write_text
    call in install_all) so the test hermeticity guard has a single,
    stable, mockable write boundary for this surface too."""
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8", newline=newline)


def unlink_shim_file(path):
    """Delete a generated shim/AutoRun file (claude.bat, forge-autorun.cmd).
    restore_all()'s tagged-shim cleanup routes through this rather than
    calling Path.unlink() directly, mirroring write_shim_file above and
    banner.py's _unlink_path -- a single, stable, mockable delete boundary
    for the test hermeticity guard (3rd real-machine-incident hardening,
    P0 finding: every direct writer/deleter must be guardable, not just the
    ones the original guard happened to cover)."""
    try:
        Path(path).unlink()
    except FileNotFoundError:
        pass


def install_into_file(path, body, marker_start=MARKER_START, marker_end=MARKER_END):
    path = Path(path)
    # utf-8-sig: a BOM'd profile (Windows tooling default) must be stripped
    # on read, not preserved verbatim and rewritten unnormalized.
    existing = path.read_text(encoding="utf-8-sig") if path.is_file() else ""
    new_text, action = upsert_marker_block(existing, body, marker_start, marker_end)
    if action != "unchanged":
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(new_text, encoding="utf-8", newline="\n")
    return action


def uninstall_from_file(path, marker_start=MARKER_START, marker_end=MARKER_END):
    path = Path(path)
    if not path.is_file():
        return "absent"
    existing = path.read_text(encoding="utf-8-sig")
    new_text, removed = remove_marker_block(existing, marker_start, marker_end)
    if removed:
        path.write_text(new_text, encoding="utf-8", newline="\n")
        return "removed"
    return "absent"


def status_of_file(path, marker_start=MARKER_START, marker_end=MARKER_END):
    path = Path(path)
    if not path.is_file():
        return "no such file"
    text = path.read_text(encoding="utf-8-sig")
    return "installed" if has_marker_block(text, marker_start, marker_end) else "not installed"


