def detect_powershell_profiles():
    """Ask each installed PowerShell host for its own $PROFILE -- never
    guess the Documents\\WindowsPowerShell layout by hand (OneDrive
    redirection, PS7 vs 5.1, CDXML host differences all move $PROFILE)."""
    profiles = []
    for exe in ("powershell", "pwsh"):
        if shutil.which(exe) is None:
            continue
        try:
            result = subprocess.run(
                [exe, "-NoProfile", "-Command", "$PROFILE"],
                capture_output=True, text=True, timeout=15,
            )
            value = result.stdout.strip()
            if result.returncode == 0 and value:
                profiles.append((exe, Path(value)))
        except Exception:
            continue
    return profiles


def detect_shell_rc_files(home=None):
    home_path = Path(home) if home else Path.home()
    candidates = [("bash", home_path / ".bashrc"), ("zsh", home_path / ".zshrc")]
    return [(shell, path) for shell, path in candidates if path.is_file()]


def default_shim_dir(home=None):
    home_path = Path(home) if home else Path.home()
    return home_path / SHIM_DIRNAME


def user_settings_path(home=None):
    """~/.claude/settings.json (or under an injected home=) -- a lazy
    FUNCTION rather than a module-level Path.home() constant, so a home=
    parameter passed down from install_all/restore_all/dedupe_user_level_
    orn_motd can actually redirect it (the prior module-level
    USER_SETTINGS_PATH constant was resolved once at import time against
    the REAL Path.home() and could never honor a test's home= override --
    part of the 3rd real-machine-incident hardening)."""
    home_path = Path(home) if home else Path.home()
    return home_path / ".claude" / "settings.json"


