# User-level orn-motd dedupe -- the plugin now ships its own welcome-area
# hook (hooks/hooks.json -> hooks/scripts/orn-motd.sh, registered
# unconditionally as part of the plugin), which does exactly what the
# 2026-07-18 hand-wired fix did: emit {"systemMessage": ...} from
# banner.py --hook. If a user-level SessionStart entry referencing
# 'orn-motd' (e.g. the hand fix at C:\Users\<user>\.claude\orn-motd.py,
# wired into ~/.claude/settings.json) survives alongside the now-active
# plugin hook, the örn art prints TWICE every session start. This detects
# and deregisters exactly that duplicate. It never deletes the underlying
# script file it didn't create -- only the settings.json wiring, so the
# change is restore-safe (a user who wants the old hand-wired hook back
# can always re-add the entry by hand; the file itself is left untouched).
# ---------------------------------------------------------------------------

def find_orn_motd_hook_indices(hooks_section):
    """Pure: given the 'hooks' dict from a settings.json-shaped document
    (lifecycle -> list of matcher-groups -> list of hook entries), return a
    list of (lifecycle, group_index, hook_index) for every hook entry whose
    command references 'orn-motd'."""
    hits = []
    for lifecycle, groups in (hooks_section or {}).items():
        for gi, group in enumerate(groups or []):
            for hi, hook in enumerate((group or {}).get("hooks", []) or []):
                if "orn-motd" in hook.get("command", ""):
                    hits.append((lifecycle, gi, hi))
    return hits


def remove_orn_motd_hooks(settings_data):
    """Pure, no I/O: returns (new_settings_data, removed_count).

    Removes exactly the hook entries whose command references 'orn-motd'
    from settings_data["hooks"], pruning now-empty matcher-groups and
    now-empty lifecycle lists, and leaves every other hook (any lifecycle,
    any tool) and every other top-level settings.json key untouched.
    Idempotent: a second call on the already-cleaned data returns it
    unchanged with removed_count 0.
    """
    data = dict(settings_data) if settings_data else {}
    hooks = data.get("hooks")
    if not hooks:
        return data, 0

    removed = 0
    new_hooks = {}
    for lifecycle, groups in hooks.items():
        new_groups = []
        for group in groups or []:
            hook_list = (group or {}).get("hooks", []) or []
            kept = [h for h in hook_list if "orn-motd" not in h.get("command", "")]
            removed += len(hook_list) - len(kept)
            if kept:
                new_group = dict(group)
                new_group["hooks"] = kept
                new_groups.append(new_group)
            # else: the whole matcher-group was only the orn-motd entry -- drop it.
        if new_groups:
            new_hooks[lifecycle] = new_groups
        # else: the whole lifecycle had nothing left -- drop the key.

    if removed == 0:
        return data, 0

    new_data = dict(data)
    if new_hooks:
        new_data["hooks"] = new_hooks
    else:
        new_data.pop("hooks", None)
    return new_data, removed


def dedupe_user_level_orn_motd(settings_path=None, read_fn=None, write_fn=None, home=None):
    """Detect + deregister a hand-wired user-level SessionStart hook
    referencing 'orn-motd' from ~/.claude/settings.json (or an injected
    path/read_fn/write_fn triple, for tests -- mirrors the get_fn/set_fn
    registry-stub pattern used for AutoRun below). Only ever touches the
    settings.json wiring; never deletes the script file it points at.
    Returns a human-readable report line, same style as install_all()/
    uninstall_all()'s other per-surface lines.
    """
    path = Path(settings_path) if settings_path else user_settings_path(home=home)

    if read_fn is not None:
        data = read_fn()
    else:
        if not path.is_file():
            return "user-level orn-motd hook: none found (no ~/.claude/settings.json)"
        try:
            # utf-8-sig: see _installed_plugin_root -- a BOM'd settings.json
            # (Windows tooling default) must not look "unreadable" here.
            data = json.loads(path.read_text(encoding="utf-8-sig"))
        except Exception:
            return "user-level orn-motd hook: settings.json unreadable, skipped"

    new_data, removed = remove_orn_motd_hooks(data)
    if removed == 0:
        return "user-level orn-motd hook: none found (nothing to dedupe)"

    if write_fn is not None:
        write_fn(new_data)
    else:
        try:
            write_shim_file(path, json.dumps(new_data, indent=2) + "\n")
        except Exception:
            return (f"user-level orn-motd hook: found {removed} but could not "
                     "remove (settings.json not writable) -- remove it by hand "
                     "to avoid the örn art printing twice")

    plural = "y" if removed == 1 else "ies"
    return (f"user-level orn-motd hook: removed {removed} duplicate entr{plural} "
            "from ~/.claude/settings.json (the plugin's own welcome-area hook "
            "now covers this; the script file itself was left in place)")


