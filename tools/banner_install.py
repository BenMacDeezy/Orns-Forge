_NO_TARGET_SENTINEL = "none"  # case-insensitive; means "the preview found no patchable target"


def main(argv=None):
    parser = argparse.ArgumentParser(
        prog="banner_install.py",
        description=("Install (binary patch + launcher shim, single mode) / "
                      "restore / status the Forge örn banner takeover."),
    )
    parser.add_argument("action",
                         choices=["install", "restore", "uninstall", "status"])
    parser.add_argument(
        "--yes", action="store_true",
        help=("Actually perform the install (binary patch + shims). Without "
              "this flag, 'install' only PREVIEWS what would happen -- the "
              "confirm-before-patch step commands/banner.md relies on."),
    )
    parser.add_argument(
        "--target",
        help=(f"F3 confirm-to-write binding: the exact target path captured "
              f"from a prior `install` (no --yes) preview. REQUIRED with "
              f"--yes -- pass the literal value '{_NO_TARGET_SENTINEL}' if "
              f"the preview reported no patchable target at all."),
    )
    parser.add_argument(
        "--expect-hash",
        help=("F3 confirm-to-write binding: the target's whole-file sha256 "
              f"captured from that same preview. REQUIRED with --yes -- "
              f"pass '{_NO_TARGET_SENTINEL}' alongside --target "
              f"'{_NO_TARGET_SENTINEL}' when there was no target."),
    )
    parser.add_argument(
        "--force", action="store_true",
        help=("Patch even a 'low-count' plan (see banner.py's "
              "_LOW_COUNT_THRESHOLD) -- only use this after deliberately "
              "reviewing the low-count warning; never passed automatically."),
    )
    args = parser.parse_args(argv)

    if args.action == "install":
        if args.yes and (not args.target or not args.expect_hash):
            lines = [
                "Refusing: install --yes requires --target and --expect-hash "
                "captured from a prior `install` (no --yes) preview run -- "
                "this binds the confirmed plan to the actual write (F3). Run "
                "`install` first, read the reported target/sha256 (or note "
                f"there was none), then re-run with --yes --target <path> "
                f"--expect-hash <hash> (or both '{_NO_TARGET_SENTINEL}' if "
                "the preview found no patchable target).",
            ]
        elif args.yes:
            target = None if args.target.lower() == _NO_TARGET_SENTINEL else args.target
            expect_hash = None if args.expect_hash.lower() == _NO_TARGET_SENTINEL else args.expect_hash
            lines = install_all(confirmed=True, target=target,
                                 expect_hash=expect_hash, force=args.force)
        else:
            lines = install_all(confirmed=False)
    elif args.action in ("restore", "uninstall"):
        lines = restore_all()
    else:
        lines = status_all()

    for line in lines:
        print(line)
    return 0


if __name__ == "__main__":
    sys.exit(main())
