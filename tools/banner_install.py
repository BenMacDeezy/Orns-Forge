def install_all(confirmed=False, home=None, target=None, expect_hash=None, force=False):
    """Single-mode install.

    F4 -- no patch-only partials: BOTH plans (can the launcher shim even
    be installed? what would the binary patch do?) are effectively
    resolved before any write happens, and if the launcher can't proceed
    at all (no `claude` CLI resolvable on PATH), the ENTIRE install is
    refused -- nothing is written, not even a binary patch that might
    otherwise have been resolvable via the native-install-root fallback a
    PATH-only shim search doesn't see. Write order is shims FIRST, patch
    LAST: if any individual shim surface fails outright, the patch step
    is skipped entirely and the partial-shim state is reported honestly,
    rather than leaving a patched binary behind a broken shim install.

    F3 -- confirm bound to write: target/expect_hash (captured by the
    caller from a PRIOR report_patch_plan() preview) are forwarded
    straight through to apply_patch, which refuses if the resolved target
    or its current hash differs from what was actually confirmed.

    confirmed=False (the default) performs NO writes at all -- it only
    returns the same report lines a real install would produce, for the
    confirm step to show the human first. Pass confirmed=True only after
    that confirm succeeds."""
    if not confirmed:
        plan_lines, _ = report_patch_plan(home=home, target_path=target)
        return (["DRY RUN -- nothing installed or patched. Re-run confirmed "
                  "to actually apply this."] + plan_lines)

    report = []

    # --- Phase 1: resolve everything needed for BOTH plans -- no writes yet.
    claude_path = resolve_claude_cli_path()
    python_path = resolve_python_interpreter()
    banner_py_path = resolve_banner_py_path()

    if claude_path is None:
        return ["claude CLI not found on PATH -- refusing the ENTIRE "
                "install (patch-only partial installs are not allowed); "
                "nothing was written, including no binary patch even "
                "though one might otherwise have been resolvable via the "
                "native-install-root fallback."]

    # Placeholder-proof guard (3rd real-machine incident hardening): a
    # resolved claude_path that does not exist as a real file (a stale
    # PATH entry, a broken shim, a hand-edited placeholder) must refuse the
    # whole install exactly like claude_path is None above -- proceeding
    # would bake the broken path into every generated shim.
    if not Path(claude_path).is_file():
        return [f"claude CLI resolved to {claude_path!r} on PATH, but that "
                "path does not exist as a real file -- refusing the ENTIRE "
                "install (placeholder/broken-path guard); nothing was "
                "written. Fix or remove whatever put that path on PATH "
                "(a stale shim, a hand-edited launcher) and re-run install."]

    # Legacy-artifact preflight (report-only scan reused as a gate here,
    # but ONLY for findings this plugin can unambiguously attribute to
    # itself -- see scan_legacy_artifacts's "gating" docstring): a broken
    # legacy shim/AutoRun entry from a prior, unclean install of OURS must
    # be cleared with restore before a fresh install writes on top of it --
    # otherwise the new install can leave two conflicting `claude` shims
    # active at once (the exact doskey-beats-PATH incident shape). A
    # non-gating finding (some unrelated program's own broken AutoRun
    # entry) is real but not ours to refuse an install over, or to promise
    # restore will clear -- it's surfaced by status_all instead.
    gating_findings = [f["message"] for f in scan_legacy_artifacts(home=home)
                        if f["gating"]]
    if gating_findings:
        return (["Refusing install -- broken legacy banner-shim artifacts "
                  "were found on this machine. Run `/forge:banner --restore` "
                  "first to clear them, then re-run install:"]
                + [f"  - {finding}" for finding in gating_findings])

    # --- Phase 2: write the launcher shim FIRST, across every surface.
    # Every surface below is wrapped in its own try/except: a failure on ONE
    # surface (e.g. PermissionError on a locked PowerShell $PROFILE) must
    # not crash the whole multi-surface operation and leave every later
    # surface untried -- record a "FAILED -- <error>" report line and keep
    # going, same style as dedupe_user_level_orn_motd()'s own failure lines.
    ps_body = build_powershell_body(claude_path, python_path, banner_py_path)
    ps_profiles = detect_powershell_profiles()
    for shell, profile_path in ps_profiles:
        try:
            action = install_into_file(profile_path, ps_body)
            report.append(f"{shell} profile ({profile_path}): {action}")
        except Exception as exc:
            report.append(f"{shell} profile ({profile_path}): FAILED -- {exc}")
    if not ps_profiles:
        report.append("no PowerShell host found on PATH -- skipped.")

    bash_body = build_bash_body(claude_path, python_path, banner_py_path)
    rc_files = detect_shell_rc_files(home=home)
    for shell, rc_path in rc_files:
        try:
            action = install_into_file(rc_path, bash_body)
            report.append(f"{shell} rc ({rc_path}): {action}")
        except Exception as exc:
            report.append(f"{shell} rc ({rc_path}): FAILED -- {exc}")
    if not rc_files:
        report.append("no ~/.bashrc or ~/.zshrc found -- skipped (nothing to adopt).")

    if os.name == "nt":
        shim_dir = default_shim_dir(home=home)
        claude_bat = shim_dir / CLAUDE_BAT_NAME
        autorun_cmd = shim_dir / AUTORUN_CMD_NAME
        shim_written = False
        try:
            write_shim_file(claude_bat, build_claude_bat(claude_path, python_path, banner_py_path))
            write_shim_file(autorun_cmd, build_autorun_cmd(claude_bat))
            report.append(f"cmd shim written: {claude_bat}")
            shim_written = True
        except Exception as exc:
            report.append(f"cmd shim ({claude_bat}): FAILED -- {exc}")

        if winreg is not None:
            if shim_written:
                try:
                    action, _ = install_autorun(autorun_cmd)
                    report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): {action}")
                except Exception as exc:
                    report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): FAILED -- {exc}")
            else:
                report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): "
                               "skipped, shim write failed")
        else:
            report.append("cmd AutoRun: winreg unavailable, skipped.")
    else:
        report.append("cmd.exe shim: not on Windows, skipped.")

    report.append('Note: Win+R "claude" (Run dialog) bypasses every shim above '
                   "-- out of scope, not wrapped.")

    # The plugin's own welcome-area hook (hooks/scripts/orn-motd.sh) is
    # shipped and active as soon as the plugin is installed -- independent
    # of this launcher-shim install. If a hand-wired user-level duplicate
    # is still registered, it would double-print the örn art every session.
    try:
        report.append(dedupe_user_level_orn_motd(home=home))
    except Exception as exc:
        report.append(f"user-level orn-motd hook: FAILED -- {exc}")

    # --- Phase 3: patch LAST (F4), and only if every shim surface above
    # actually succeeded -- a FAILED line anywhere means an incomplete
    # shim install, and this refuses to layer a binary patch on top of
    # that rather than reporting a false "fully installed" state.
    shim_failed = any(" FAILED -- " in line for line in report)
    if shim_failed:
        report.append("binary patch: skipped -- one or more shim surfaces "
                       "above FAILED; reporting the partial-shim state "
                       "honestly rather than patching on top of an "
                       "incomplete install. Fix the failure(s) above and "
                       "re-run install to complete the patch.")
    else:
        cli_version = _detect_claude_cli_version(claude_path)
        patch_result = banner_patch.apply_patch(
            target_path=target, home=home, cli_version=cli_version,
            force=force, expect_target=target, expect_hash=expect_hash)
        report.append(f"binary patch ({patch_result.get('target')}): "
                       f"{patch_result['status']} -- {patch_result['message']}")

    return report


def restore_all(home=None):
    """`/forge:banner --restore`: byte-exactly restores the patched binary
    AND removes every shim/wrapper/AutoRun entry this plugin installed,
    AND removes the takeover stamp. Always available, no confirm needed --
    it only ever reverts state this plugin itself created."""
    report = []
    patch_result = banner_patch.restore_patch(home=home)
    report.append(f"binary restore ({patch_result.get('target')}): "
                   f"{patch_result['status']} -- {patch_result['message']}")

    for shell, profile_path in detect_powershell_profiles():
        try:
            action = uninstall_from_file(profile_path)
            report.append(f"{shell} profile ({profile_path}): {action}")
        except Exception as exc:
            report.append(f"{shell} profile ({profile_path}): FAILED -- {exc}")

    for shell, rc_path in detect_shell_rc_files(home=home):
        try:
            action = uninstall_from_file(rc_path)
            report.append(f"{shell} rc ({rc_path}): {action}")
        except Exception as exc:
            report.append(f"{shell} rc ({rc_path}): FAILED -- {exc}")

    if os.name == "nt":
        shim_dir = default_shim_dir(home=home)
        claude_bat = shim_dir / CLAUDE_BAT_NAME
        autorun_cmd = shim_dir / AUTORUN_CMD_NAME
        if winreg is not None:
            try:
                action, new_value = uninstall_autorun(autorun_cmd)
                report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): {action}")
                # "absent" covers two different situations: genuinely never
                # installed (new_value falsy) vs. the registry value holds
                # something that doesn't match our invocation at all (a
                # mismatch). Silently deleting the shim files in the latter
                # case would leave a dangling AutoRun entry pointing at a
                # now-missing file -- still clean up our own artifacts
                # below, but surface an explicit warning so the user knows
                # to check the registry by hand.
                if action == "absent" and new_value:
                    report.append(
                        f"WARNING: cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}) still "
                        f"holds a value that does not reference the forge shim "
                        f"({new_value!r}) -- check it by hand; shim files are "
                        "still being removed below."
                    )
            except Exception as exc:
                report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): FAILED -- {exc}")
        for shim_file, tag in ((claude_bat, FORGE_BAT_TAG), (autorun_cmd, FORGE_AUTORUN_TAG)):
            try:
                if shim_file.is_file():
                    try:
                        text = shim_file.read_text(encoding="utf-8", errors="ignore")
                    except OSError:
                        text = ""
                    if tag in text:
                        unlink_shim_file(shim_file)
                        report.append(f"cmd shim removed: {shim_file}")
                    else:
                        report.append(f"cmd shim ({shim_file}): not forge-authored, left alone")
                else:
                    report.append(f"cmd shim ({shim_file}): absent")
            except Exception as exc:
                report.append(f"cmd shim ({shim_file}): FAILED -- {exc}")

    # Uninstalling the launcher shim does not affect the plugin's own
    # welcome-area hook (hooks/scripts/orn-motd.sh) -- that stays wired via
    # hooks.json regardless. A stale user-level duplicate would still
    # double-print the örn art, so dedupe here too.
    try:
        report.append(dedupe_user_level_orn_motd(home=home))
    except Exception as exc:
        report.append(f"user-level orn-motd hook: FAILED -- {exc}")
    return report


# uninstall_all is the pre-takeover name; kept as an alias since it is the
# exact same operation now that restore always includes it (nothing to
# migrate callers off of -- both names do the full restore).
uninstall_all = restore_all


def status_all(home=None):
    report = []
    patch_stamp = banner_patch._read_stamp(banner_patch._patch_stamp_path(home))
    if patch_stamp:
        report.append(f"binary patch (target: {patch_stamp.get('target')}): "
                       f"installed (stamp: {banner_patch._patch_stamp_path(home)})")
    else:
        report.append("binary patch: not installed (no stamp found)")
    for shell, profile_path in detect_powershell_profiles():
        report.append(f"{shell} profile ({profile_path}): {status_of_file(profile_path)}")
    for shell, rc_path in detect_shell_rc_files(home=home):
        report.append(f"{shell} rc ({rc_path}): {status_of_file(rc_path)}")
    if os.name == "nt":
        shim_dir = default_shim_dir(home=home)
        claude_bat = shim_dir / CLAUDE_BAT_NAME
        autorun_cmd = shim_dir / AUTORUN_CMD_NAME
        report.append(f"cmd shim ({claude_bat}): "
                       f"{'present' if claude_bat.is_file() else 'absent'}")
        if winreg is not None:
            existing = get_autorun_value()
            our_invocation = build_autorun_invocation(autorun_cmd)
            if existing and our_invocation in existing:
                state = "installed"
            elif existing:
                state = f"AutoRun set to something else: {existing!r}"
            else:
                state = "not installed"
            report.append(f"cmd AutoRun (HKCU\\{REGISTRY_KEY_PATH}): {state}")
        else:
            report.append("cmd AutoRun: winreg unavailable, cannot check.")

    # Legacy-artifact scan (report-only): a broken shim/AutoRun entry left
    # behind by an unclean prior install/uninstall (e.g. the doskey-beats-
    # PATH incident -- see commands/banner.md) manifests as `claude` failing
    # in cmd.exe while `where claude` still looks correct. Surfaced here so
    # `/forge:banner status` can catch it without the user having to debug
    # a launch failure blind.
    legacy_findings = scan_legacy_artifacts(home=home)
    if legacy_findings:
        for finding in legacy_findings:
            label = "legacy scan" if finding["gating"] else "legacy scan (informational, not ours)"
            report.append(f"{label}: {finding['message']}")
    else:
        report.append("legacy scan: no broken legacy banner-shim artifacts found")
    return report


